print("Ingresa un número:")
numero = int(input())

# Evalúa si el número es menor a 100.
print(numero < 100)

# Evalúa si el número es mayor a 100.
print(numero > 100)

# Evalúa si el número es menor o igual a 100.
print(numero <= 100)

# Evalúa si el número es mayor o igual a 100.
print(numero >= 100)

# Evalúa si el número es igual a 100.
print(numero == 100)

# Evalúa si el número es diferente de 100.
print(numero != 100)