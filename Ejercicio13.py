def calcular_volumen(largo, ancho, altura):
    # La documentación descibe que hace la función, clase o módulo.
    """
    Calcula el volumen de una caja expresado en centímetros cúbicos.
    
    Args:
        largo (float): Largo de la caja.
        ancho (float): Ancho de la caja.
        altura (float): Altura de la caja.
        
    returns:
        float: Volumen de la caja
    """
    return largo * ancho * altura